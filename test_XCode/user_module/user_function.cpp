#include "user_function.h"

int user_sum_fuction (int first, int second)
{
    return first + second;
}
double user_sum_fuction (double first, double second)
{
    return first + second;
}

int user_multiply_function (int first, int second)
{
    return first * second;
}
double user_multiply_function (double first, double second)
{
    return first * second;
}
